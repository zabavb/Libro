                    subCategories.Add(new SubCategory
                    {
                        Id = Guid.NewGuid(),
                        CategoryId = categories[i].Id,
                        Name = SubCategoryNames[i][j]
                    });
                }
            }
            modelBuilder.Entity<SubCategory>().HasData(subCategories);

            var publishers = new List<Publisher>();

