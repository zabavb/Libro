        public static Dictionary<string, string> GenerateFileNames(string[] titles, string extension = ".jpeg")
        {
            var result = new Dictionary<string, string>();

            foreach (var title in titles)
            {
                var fileName = title
                    .ToLowerInvariant()
                    .Replace("’", "")
                    .Replace("'", "")
                    .Replace(",", "")
                    .Replace(":", "")
                    .Replace("–", "-")
                    .Replace("—", "-")
                    .Replace("«", "")
                    .Replace("»", "")
                    .Replace(".", "")
                    .Replace("?", "")
                    .Replace("!", "")
                    .Replace(" ", "_");

                result[title] = $"Data/Files/Images/{fileName}{extension}";
            }

            return result;
        }

