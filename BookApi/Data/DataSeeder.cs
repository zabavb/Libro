            modelBuilder.Entity<Category>().HasData(categories);

            var subCategories = new List<SubCategory>();

            for (int i = 0; i < SubCategoryNames.Length; i++)
            {
                for (int j = 0; j < SubCategoryNames[i].Length; j++)
